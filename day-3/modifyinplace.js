let n = 2;
n = n + 3
n = n + 5
console.log(n); // 10

// Alternatively, we can use the += or *= operator to add a value to a variable
// and assign the result to the same variable in one step:
 let n1 = 3
 n1 += 5 // n1 = n1 + 5
 n1 *= 2 // n1 = n1 * 2
 console.log(n1); // 16
 // modify and assign operators work with all arithmetic operators and bitwise operators example /=, %=, &=, -=, |=, ^=, <<=, >>=, >>>=
