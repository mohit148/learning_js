// expressions in JavaScript
// An expression is a piece of code that produces a value
// Examples of expressions
"hi"; // string expression -> produces a string value
true// boolean expression -> produces a boolean value
42; // number expression -> produces a number value
// 1 + 2; // arithmetic expression -> produces a number value
// "hi" + " there"; // string concatenation expression -> produces a string value
