// Literals
// A literal is a fixed value written directly in code that represents a specific data type, like numbers (10), strings ("hello"),
// booleans (true), null, arrays ([1, 2]), or objects ({ key: "value" }). It's the actual value, not a variable or expression.

// Every literal has a data type, but

// Not every value of a data type is a literal.

